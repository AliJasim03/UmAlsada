    private void addMemBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMemBtnActionPerformed
        String gender = null;
        if (maleRadioButton.isSelected()) {
            gender = "Male";
        } else if (femaleRadioButton.isSelected()) {
            gender = "Female";
        }
        try {
            mem.setFirstName(txtFm.getText());
            mem.setLastName(txtLm.getText());
            mem.setBirthDate(txtDm.getText());
            mem.setGender(gender);
            mem.setAddress(txtAm.getText());
            mem.setPhone(txtPhon.getText());
            if (student.isSelected()) {
                ((PolytechnicStudent) mem).setMajor(txtPositionOrMajor.getText());
                ((PolytechnicStudent) mem).setTeam(txtDepartmentOrTeam.getText());
            } else if (staff.isSelected()) {
                ((PolytechnicStaff) mem).setPosition(txtPositionOrMajor.getText());
                ((PolytechnicStaff) mem).setDepartment(txtDepartmentOrTeam.getText());

            }

            manageMembersPnl.showData();
            Message obj = new Message();
            obj.jLabel1.setText("Data has been updated");
            GlassPanePopup.showPopup(obj);
            obj.eventOK(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    GlassPanePopup.closePopupLast();
                    MainFrame.body.removeAll();
                    MainFrame.body.add(manageMembersPnl);
                    MainFrame.body.repaint();
                    MainFrame.body.revalidate();
                }
            });
            FileManager.getInstance().writeMember();
        } catch (Exception e) {
            obj.jLabel1.setText("Please check your input data");
            GlassPanePopup.showPopup(obj);
        }

    }//GEN-LAST:event_addMemBtnActionPerformed

