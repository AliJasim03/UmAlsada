    public void showData() {
        txtFm.setText(mem.getFirstName());
        txtLm.setText(mem.getLastName());
        txtAm.setText(mem.getAddress());
        txtDm.setText(mem.getBirthDate());
        txtPhon.setText(mem.getPhone());
        if (mem.getGender().equalsIgnoreCase("male")) {
            maleRadioButton.setSelected(true);
        } else {
            femaleRadioButton.setSelected(true);
        }
        if (mem instanceof PolytechnicStudent) {
            student.setSelected(true);
            txtPositionOrMajor.setText(((PolytechnicStudent) mem).getMajor());
            txtDepartmentOrTeam.setText(((PolytechnicStudent) mem).getTeam());
        } else if (mem instanceof PolytechnicStaff) {
            staff.setSelected(true);
            txtPositionOrMajor.setText(((PolytechnicStaff) mem).getPosition());
            txtDepartmentOrTeam.setText(((PolytechnicStaff) mem).getDepartment());
        }
    }
